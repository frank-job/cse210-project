using System;

class Program
{
    static void Main(string[] args)
    {
       
        Address address1 = new Address("123 Main St", "New York", "NY", "USA");
        Customer customer1 = new Customer("Frank Tech", address1);
        Order order1 = new Order(customer1);

        order1.AddProduct(new Product("Laptop", "L100", 1000, 1));
        order1.AddProduct(new Product("Mouse", "M200", 25, 2));

       
        Address address2 = new Address("45 Nile Road", "Jinja", "Eastern", "Uganda");
        Customer customer2 = new Customer("John Doe", address2);
        Order order2 = new Order(customer2);

        order2.AddProduct(new Product("Solar Panel", "S500", 200, 3));
        order2.AddProduct(new Product("Battery", "B300", 150, 1));

      
        
        Console.WriteLine("---------------- ORDER 1 ----------------");
        Console.WriteLine(order1.GetPackingLabel());
        Console.WriteLine(order1.GetShippingLabel());
        Console.WriteLine($"Total Cost: ${order1.CalculateTotalCost()}");

        Console.WriteLine("\n---------------- ORDER 2 ----------------");
        Console.WriteLine(order2.GetPackingLabel());
        Console.WriteLine(order2.GetShippingLabel());
        Console.WriteLine($"Total Cost: ${order2.CalculateTotalCost()}");
    }
}